clear all;

home_figures_path = '/home/yusipov/Work/os_d/figures';

data_path = '/data/biophys/yusipov/os_d';
prefix = 'qjx_results';

data_path = sprintf('%s/%s', data_path, prefix);

sys_id = 1; 
task_id = 0; 
prop_id = 0; 
seed = 0; 
mns = 1000000;
num_trajectories = 50;
num_tp_periods = 1000;
num_obs_periods = 5000; 
ex_deep = 16;
rk_ns = 10000;
 
lpn = 1.0e-2;
 
N = 200; 
